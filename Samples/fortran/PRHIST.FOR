      SUBROUTINE PRHIST(A,X,Y,DIM1,DIM2)
C   PRINTS A CONSOLE HISTOGRAM OF ENTRY DATA POINTS
C     A    -  ARRAY OF ENTRY POINTS
C     X    -  ARRAY OF X-AXIS VALUES
C     Y    -  ARRAY OF Y-AXIS VALUES
C     DIM1 -  FIRST DIMENSION OF A; NUMBER OF INTERVALS
C     DIM2 -  SECONT DIMENSION OF A; NUMBER OF ROWS
      INTEGER   DIM1,DIM2
      REAL      A,X,Y
      DIMENSION A(DIM1,DIM2),X(DIM1),Y(DIM2)
      INTEGER   I,J
      CHARACTER STR(80),PART(17)
      
      IF(MAX(ABS(Y(1:DIM2))) .GT. 1.0)THEN
        WRITE(UNIT=PART,FMT='(A)') '2X,E8.2,1X,"|",1X'
      ELSE
        WRITE(UNIT=PART,FMT='(A)') '2X,F8.6,1X,"|",1X'
      END IF
      DO 10 J=1,DIM2
        
   10 CONTINUE
      RETURN
   50 FORMAT(' ')
 1000 FORMAT(2X,E8.2,'|',F10.8)
      END